// file: arithop.c

#include <stdio.h>

int main(void)
{
	printf("3 + 4 ==> %d\n", 3 + 4);	//3 + 4 ==> 7
	printf("3.4 - 4.3 ==> %lf\n", 3.4 - 4.3);	//3.4 - 4.3 ==> -0.900000
	printf("3.4 * 4.3 ==> %lf\n", 3.4 * 4.3);	//3.4 * 4.3 ==> 14.520000
	printf("10 / 3 ==> %d\n", 10 / 3);	//10 / 3 ==> 3
	printf("10.0 / 3.0 ==> %lf\n", 10.0 / 3.0);	//10.0 / 3.0 ==> 3.333333
	printf("10 %% 3 ==> %d\n", 10 % 3);	//10 % 3 ==> 1

	return 0;
}